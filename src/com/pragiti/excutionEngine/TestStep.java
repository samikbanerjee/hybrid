package com.pragiti.excutionEngine;

public class TestStep {

	private String TestCaseId;
	private String KeyWord;
	private String ElementIdentifier;
	private String ElementIdentifierValue;
	private String InputValue;
	private String AssertValue;
	
	public String getTestCaseId() {
		return TestCaseId;
	}
	public void setTestCaseId(String testCaseId) {
		TestCaseId = testCaseId;
	}
	public String getKeyWord() {
		return KeyWord;
	}
	public void setKeyWord(String keyWord) {
		KeyWord = keyWord;
	}
	public String getElementIdentifier() {
		return ElementIdentifier;
	}
	public void setElementIdentifier(String elementIdentifier) {
		ElementIdentifier = elementIdentifier;
	}
	public String getElementIdentifierValue() {
		return ElementIdentifierValue;
	}
	public void setElementIdentifierValue(String elementIdentifierValue) {
		ElementIdentifierValue = elementIdentifierValue;
	}
	public String getInputValue() {
		return InputValue;
	}
	public void setInputValue(String inputValue) {
		InputValue = inputValue;
	}
	public String getAssertValue() {
		return AssertValue;
	}
	public void setAssertValue(String assertValue) {
		AssertValue = assertValue;
	}
	
	
	
}
